package top.yeaho.entities;


public class Module {
	private String moduleName;
	private String moduleLink;
	private String tip;
	private String moduleBerif;
	private String moduleKeywords;
	private String moduleAuthor;
	private String moduleDescription;
	private String pageTitle;
	
	
	public String getPageTitle() {
		return pageTitle;
	}
	public void setPageTitle(String pageTitle) {
		this.pageTitle = pageTitle;
	}
	public String getModuleDescription() {
		return moduleDescription;
	}
	public void setModuleDescription(String moduleDescription) {
		this.moduleDescription = moduleDescription;
	}
	public String getModuleKeywords() {
		return moduleKeywords;
	}
	public void setModuleKeywords(String moduleKeywords) {
		this.moduleKeywords = moduleKeywords;
	}
	public String getModuleAuthor() {
		return moduleAuthor;
	}
	public void setModuleAuthor(String moduleAuthor) {
		this.moduleAuthor = moduleAuthor;
	}
	public String getModuleName() {
		return moduleName;
	}
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}
	public String getTip() {
		return tip;
	}
	public void setTip(String tip) {
		this.tip = tip;
	}
	public String getModuleLink() {
		return moduleLink;
	}
	public void setModuleLink(String moduleLink) {
		this.moduleLink = moduleLink;
	}
	public String getModuleBerif() {
		return moduleBerif;
	}
	public void setModuleBerif(String moduleBerif) {
		this.moduleBerif = moduleBerif;
	}
	
	
}
