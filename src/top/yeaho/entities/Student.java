package top.yeaho.entities;

public class Student {
	private int id;
	private String studentNo;
	private String studentName;
	private String studentCampus;
	private String studentCompany;
	private String studentClass;
	private String studentGender;
	private String studentTel;
	private String studentEmail;
	private String studentLoginInfor;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStudentNo() {
		return studentNo;
	}
	public void setStudentNo(String studentNo) {
		this.studentNo = studentNo;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getStudentClass() {
		return studentClass;
	}
	public void setStudentClass(String studentClass) {
		this.studentClass = studentClass;
	}
	public String getStudentGender() {
		return studentGender;
	}
	public void setStudentGender(String studentGender) {
		this.studentGender = studentGender;
	}
	public String getStudentTel() {
		return studentTel;
	}
	public void setStudentTel(String studentTel) {
		this.studentTel = studentTel;
	}
	public String getStudentEmail() {
		return studentEmail;
	}
	public void setStudentEmail(String studentEmail) {
		this.studentEmail = studentEmail;
	}
	public String getStudentLoginInfor() {
		return studentLoginInfor;
	}
	public void setStudentLoginInfor(String studentLoginInfor) {
		this.studentLoginInfor = studentLoginInfor;
	}
	public String getStudentCampus() {
		return studentCampus;
	}
	public void setStudentCampus(String studentCampus) {
		this.studentCampus = studentCampus;
	}
	public String getStudentCompany() {
		return studentCompany;
	}
	public void setStudentCompany(String studentCompany) {
		this.studentCompany = studentCompany;
	}
	
	
}
