package top.yeaho.entities;

public class School {
	private Integer id;
	private String schoolName;
	private String schoolPassword;
	private String schoolType;
	private String schoolInformation;
	private Integer status;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getSchoolPassword() {
		return schoolPassword;
	}
	public void setSchoolPassword(String schoolPassword) {
		this.schoolPassword = schoolPassword;
	}
	public String getSchoolType() {
		return schoolType;
	}
	public void setSchoolType(String schoolType) {
		this.schoolType = schoolType;
	}
	public String getSchoolInformation() {
		return schoolInformation;
	}
	public void setSchoolInformation(String schoolInformation) {
		this.schoolInformation = schoolInformation;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	
	
	
}
